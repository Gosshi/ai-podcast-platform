update public.episodes as episode
set judgment_cards = cards.payload
from (
  select
    episode_id,
    jsonb_agg(
      jsonb_build_object(
        'topic_title',
        topic_title,
        'frame_type',
        frame_type,
        'judgment_type',
        judgment_type,
        'judgment_summary',
        judgment_summary,
        'action_text',
        action_text,
        'deadline_at',
        deadline_at,
        'threshold_json',
        threshold_json,
        'watch_points_json',
        watch_points_json,
        'confidence_score',
        confidence_score
      )
      order by topic_order
    ) as payload
  from public.episode_judgment_cards
  where episode_id in (
    '10000000-0000-0000-0000-000000000001',
    '10000000-0000-0000-0000-000000000002',
    '10000000-0000-0000-0000-000000000003',
    '10000000-0000-0000-0000-000000000004',
    '10000000-0000-0000-0000-000000000005'
  )
  group by episode_id
) as cards
where episode.id = cards.episode_id;

